public class Demo4 {
    public static void main(String[] args) {
        // 整形： 整数类型
        int zheng = 1000000000;
        // long长整型： 大整数类型 （注意后面要加 大写L）
        long da = 3000000000L;
        System.out.println(zheng);
        System.out.println(da);

        // byte 1字节  -128~127
        byte b = 0;
        // short 2字节  -32768~32767
        short s = 3000;
        // int 4字节  -2亿~2亿
        int i = 2000000000;
    }
}
